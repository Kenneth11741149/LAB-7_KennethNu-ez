    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if (FatherComboBoxModel.getSelectedIndex() >= 0) {
            try {
                String Pnombre = PiezaName.getText();
                String Pmaterial = PiezaMaterial.getText();
                int Ptiempo = Integer.parseInt(PiezaTime.getText());
                Piezas nueva = new Piezas(Pnombre, Pmaterial, Ptiempo);
                if (Contains(nueva)) {
                    JOptionPane.showMessageDialog(this, "Ya existe la pieza. ");
                } else {
                    Object temporal = FatherComboBoxModel.getSelectedItem();
                    if (temporal instanceof Carro) {
                        ((Carro) temporal).getPieces().add(nueva);
                    } else {
                        ((Piezas) temporal).getPieces().add(nueva);
                    }
                    nueva.setPadre(temporal);
                    todo.add(nueva);
                    DefaultComboBoxModel model = new DefaultComboBoxModel(todo.toArray());
                    FatherComboBoxModel.setModel(model);
                    PiezaName.setText("");
                    PiezaMaterial.setText("");
                    PiezaTime.setText("");
                    FatherComboBoxModel.setSelectedIndex(0);
                    JOptionPane.showMessageDialog(this, "Se ha agregado exitosamente.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ingrese un tiempo como numeros enteros.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un padre.");
        }
    }//GEN-LAST:event_jButton2MouseClicked

